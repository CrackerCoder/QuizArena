export type UILang = "en" | "ms" | "zh" | "ta";

export function langToUI(language: Language): UILang {
  switch (language) {
    case "malay":
    case "mandarin-malay":
      return "ms";
    case "mandarin":
    case "mandarin-english":
      return "zh";
    case "tamil":
    case "tamil-english":
      return "ta";
    default:
      return "en";
  }
}

