const DICT: Record<UILang, Translations> = { en: EN, ms: MS, zh: ZH, ta: TA };

/**
 * Replaces {key} placeholders in a translation string with actual values.
 * Usage: t("youReachedLevel", { n: "5" })
 */
function interpolate(str: string, params?: Record<string, string | number>): string {
  if (!params) return str;
  return str.replace(/\{(\w+)\}/g, (_, k) =>
    params[k] !== undefined ? String(params[k]) : `{${k}}`
  );
}

/** Hook that returns a translator function bound to the current language setting. */
export function useT() {
  const { settings } = useSettings();
  const lang = langToUI(settings.language);
  const dict = DICT[lang];

  return function t(key: keyof Translations, params?: Record<string, string | number>): string {
    const raw = dict[key] ?? EN[key] ?? key;
    return interpolate(raw, params);
  };
}

/** Standalone translator for use outside React (e.g. in event callbacks with a known lang). */
export function makeT(lang: UILang) {
  const dict = DICT[lang];
  return function t(key: keyof Translations, params?: Record<string, string | number>): string {
    const raw = dict[key] ?? EN[key] ?? key;
    return interpolate(raw, params);
  };
}
